function test1() {
    let boundFunction = function () {}.bind();
    Object.defineProperty(boundFunction, "prototype", {
        get() {
            throw Error("Hello");
        }
    });

    let threw = false;
    try {
        Reflect.construct(function() {}, [], boundFunction);
    } catch(e) {
        threw = true;
        assert(e.message === "Hello");
    }
    assert(threw);
}
test1();


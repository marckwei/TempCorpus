function shouldBe(actual, expected) {
    if (actual !== expected)
        throw new Error('bad value: ' + actual);
}

class A {
    get cocoa() {
        return "Cocoa";
    }

    get cappuccino() {
        return "Cappuccino";
    }
}

let a = new A();
shouldBe(JSON.stringify(a), `{}`);
shouldBe(JSON.stringify(a, ["cocoa", "cappuccino"]), `{"cocoa":"Cocoa","cappuccino":"Cappuccino"}`);


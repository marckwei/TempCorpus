function AsmModule(glob) {
    "use asm";
    var fround = glob.Math.fround;
    function floatNeg(a) {
        a = fround(a);
        var e = fround(0);
        e = fround(-a);
        return fround(e);
    }
    function uintRem(a) {
        a = a|0;
        var b = 0;
        var c = 0;
        var d = 0;
        c = (a + d)|0;
        d = (a >>> 0) % 10 | 0;
        b = c;
        return b|0;
    }
    return {
        floatNeg:floatNeg,
        uintRem:uintRem
    }
}

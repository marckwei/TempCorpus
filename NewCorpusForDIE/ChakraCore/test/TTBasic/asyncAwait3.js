function resolveWait(x) {
    return new Promise(resolve => {
        WScript.SetTimeout(() => {
            resolve(x);
        }, 100);
    });
}

async function awaitLater(x) {
    const p_a = resolveWait(10);
    const p_b = resolveWait(20);
    return x + await p_a + await p_b;
}

